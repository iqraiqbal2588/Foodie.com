        <h3>Ingredients:</h3>
        <ul>
          <li>1 lb pork tenderloin, cut into bite-sized pieces</li>
          <li>1 cup pineapple chunks</li>
          <li>1/2 cup bell peppers, diced</li>
          <li>1/2 cup onion, diced</li>
          <li>1/4 cup carrot, diced</li>
          <li>2 cloves garlic, minced</li>
          <li>2 tablespoons soy sauce</li>
          <li>2 tablespoons ketchup</li>
          <li>2 tablespoons rice vinegar</li>
          <li>2 tablespoons brown sugar</li>
          <li>1 tablespoon cornstarch</li>
          <li>1/2 cup water</li>
          <li>2 tablespoons vegetable oil</li>
          <li>Salt and pepper to taste</li>
        </ul>

        <h3>Instructions:</h3>
        <ol>
          <li>In a small bowl, mix together soy sauce, ketchup, rice vinegar, brown sugar, cornstarch, and water. Set aside.</li>
          <li>Heat vegetable oil in a pan or wok over medium-high heat.</li>
          <li>Add minced garlic, diced onion, and diced carrot to the pan. Saut√© until onions are translucent and garlic is fragrant.</li>
          <li>Add pork pieces to the pan and cook until browned and cooked through.</li>
          <li>Add diced bell peppers and pineapple chunks to the pan. Stir-fry for a few minutes until the vegetables are slightly tender.</li>
          <li>Pour the sauce mixture over the pork and vegetables in the pan. Stir to coat everything evenly.</li>
          <li>Cook for a few more minutes, stirring occasionally, until the sauce thickens and coats the pork and vegetables.</li>
          <li>Season with salt and pepper to taste.</li>
          <li>Remove from heat and serve the Sweet and Sour Pork hot.</li>
        </ol>
