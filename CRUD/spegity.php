        <h3>Ingredients:</h3>
        <ul>
          <li>8 oz spaghetti noodles</li>
          <li>2 tablespoons olive oil</li>
          <li>4 cloves garlic, minced</li>
          <li>1 small onion, chopped</li>
          <li>1 lb ground beef</li>
          <li>1 can (14 oz) crushed tomatoes</li>
          <li>1 can (6 oz) tomato paste</li>
          <li>1 teaspoon dried oregano</li>
          <li>1 teaspoon dried basil</li>
          <li>1/2 teaspoon sugar</li>
          <li>Salt and pepper to taste</li>
          <li>Grated Parmesan cheese for garnish</li>
          <li>Fresh basil leaves for garnish</li>
        </ul>

        <h3>Instructions:</h3>
        <ol>
          <li>Cook spaghetti noodles according to package instructions. Drain and set aside.</li>
          <li>In a large pan, heat olive oil over medium heat.</li>
          <li>Add minced garlic and chopped onion to the pan. Saut√© until onions are translucent and garlic is fragrant.</li>
          <li>Add ground beef to the pan and cook until browned. Drain any excess fat.</li>
          <li>Add crushed tomatoes, tomato paste, dried oregano, dried basil, sugar, salt, and pepper to the pan. Stir well to combine.</li>
          <li>Simmer the sauce for 15-20 minutes, stirring occasionally.</li>
          <li>Add the cooked spaghetti noodles to the pan and toss everything together until the noodles are well coated with the sauce.</li>
          <li>Remove from heat and serve the spaghetti hot.</li>
          <li>Garnish with grated Parmesan cheese and fresh basil leaves, if desired.</li>
        </ol>
