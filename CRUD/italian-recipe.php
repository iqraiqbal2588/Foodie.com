      <img src="lasagna.jpg" alt="Lasagna" class="img-fluid">
      <p>
        Lasagna is a traditional Italian dish made with layers of pasta, meat or vegetable filling, and cheese. It is
        baked in the oven until the cheese is melted and bubbly, creating a delicious and hearty meal.
      </p>
      <p><strong>Ingredients:</strong></p>
      <ul>
        <li>Lasagna noodles</li>
        <li>Meat or vegetable filling (e.g., ground beef, sausage, spinach, mushrooms)</li>
        <li>Tomato sauce</li>
        <li>Cheese (e.g., mozzarella, ricotta, Parmesan)</li>
        <li>Herbs and spices (e.g., oregano, basil, garlic powder)</li>
      </ul>
      <p><strong>Instructions:</strong></p>
      <ol>
        <li>Preheat the oven to the desired temperature (usually 375째F/190째C).</li>
        <li>Cook the lasagna noodles according to package instructions until al dente.</li>
        <li>In a separate pan, cook the meat or vegetable filling until browned and cooked through.</li>
        <li>In a baking dish, spread a layer of tomato sauce on the bottom.</li>
        <li>Place a layer of cooked lasagna noodles on top of the sauce.</li>
        <li>Add a layer of the meat or vegetable filling on top of the noodles.</li>
        <li>Sprinkle cheese and herbs/spices on top of the filling.</li>
        <li>Repeat the layers until all ingredients are used, ending with a layer of noodles on top.</li>
        <li>Cover the lasagna with foil and bake in the preheated oven for about 30-40 minutes.</li>
        <li>Remove the foil and bake for an additional 10-15 minutes, or until the cheese is melted and bubbly and
          the lasagna is heated through.</li>
        <li>Remove from the oven, let it cool slightly, slice, and serve hot.</li>
