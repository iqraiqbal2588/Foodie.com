      <h3>White-Sauce Pasta</h3>
      <img src="white-sauce-pasta.jpg" alt="White-Sauce Pasta" class="img-fluid">
      <p>
        White-Sauce Pasta, also known as Alfredo pasta, is a creamy and rich pasta dish made with a white sauce
        consisting of butter, flour, milk, and cheese. It is a comforting and indulgent meal option.
      </p>
      <p><strong>Ingredients:</strong></p>
      <ul>
        <li>Pasta (e.g., fettuccine, penne)</li>
        <li>Butter</li>
        <li>All-purpose flour</li>
        <li>Milk</li>
        <li>Grated cheese (e.g., Parmesan, mozzarella)</li>
        <li>Salt and pepper</li>
        <li>Optional ingredients (e.g., garlic, mushrooms, cooked chicken)</li>
      </ul>
      <p><strong>Instructions:</strong></p>
      <ol>
        <li>Cook the pasta according to package instructions until al dente.</li>
        <li>In a separate saucepan, melt butter over medium heat.</li>
        <li>Add flour to the melted butter and whisk continuously for about 1-2 minutes to form a roux.</li>
        <li>Slowly pour in milk while whisking continuously to avoid lumps.</li>
        <li>Cook the sauce until it thickens and coats the back of a spoon.</li>
        <li>Add grated cheese to the sauce and stir until melted and smooth.</li>
        <li>Season with salt and pepper to taste.</li>
        <li>Add any optional ingredients, such as garlic, mushrooms, or cooked chicken, and cook until heated
          through.</li>
        <li>Drain the cooked pasta and add it to the sauce. Toss to coat the pasta evenly.</li>
        <li>Serve hot and enjoy!</li>
      </ol>
