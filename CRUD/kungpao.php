        <h3>Ingredients:</h3>
        <ul>
          <li>1 lb boneless, skinless chicken breasts, cut into bite-sized pieces</li>
          <li>1/2 cup roasted peanuts</li>
          <li>1/2 cup bell peppers, diced</li>
          <li>1/2 cup zucchini, diced</li>
          <li>1/4 cup onion, diced</li>
          <li>2 cloves garlic, minced</li>
          <li>2 tablespoons soy sauce</li>
          <li>1 tablespoon hoisin sauce</li>
          <li>1 tablespoon rice vinegar</li>
          <li>1 tablespoon honey</li>
          <li>1 teaspoon cornstarch</li>
          <li>1/2 teaspoon red pepper flakes (adjust to taste)</li>
          <li>1 tablespoon vegetable oil</li>
          <li>Salt and pepper to taste</li>
        </ul>

        <h3>Instructions:</h3>
        <ol>
          <li>In a small bowl, mix together soy sauce, hoisin sauce, rice vinegar, honey, cornstarch, and red pepper flakes. Set aside.</li>
          <li>Heat vegetable oil in a pan or wok over medium-high heat.</li>
          <li>Add minced garlic and diced onion to the pan. Saut√© until onions are translucent and garlic is fragrant.</li>
          <li>Add diced chicken to the pan and cook until browned and cooked through.</li>
          <li>Add diced bell peppers and zucchini to the pan. Stir-fry for a few minutes until the vegetables are slightly tender.</li>
          <li>Pour the sauce mixture over the chicken and vegetables in the pan. Stir to coat everything evenly.</li>
          <li>Add roasted peanuts to the pan and mix well.</li>
          <li>Cook for a few more minutes, stirring occasionally, until the sauce thickens and coats the chicken and vegetables.</li>
          <li>Season with salt and pepper to taste.</li>
          <li>Remove from heat and serve the Kung Pao Chicken hot.</li>
        </ol>
