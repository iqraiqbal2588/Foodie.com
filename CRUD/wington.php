        <h3>Ingredients:</h3>
        <ul>
          <li>2 cups cooked rice</li>
          <li>1 cup cooked chicken, shredded</li>
          <li>1 cup mixed vegetables (carrots, peas, corn)</li>
          <li>1/2 cup bell peppers, chopped</li>
          <li>1/2 cup onion, chopped</li>
          <li>2 cloves garlic, minced</li>
          <li>2 tablespoons soy sauce</li>
          <li>1 tablespoon oyster sauce</li>
          <li>1 tablespoon ketchup</li>
          <li>1 tablespoon chili sauce</li>
          <li>1 tablespoon vegetable oil</li>
          <li>Salt and pepper to taste</li>
        </ul>

        <h3>Instructions:</h3>
        <ol>
          <li>In a large pan or wok, heat vegetable oil over medium heat.</li>
          <li>Add minced garlic and chopped onions to the pan. Saut√© until onions are translucent.</li>
          <li>Add chopped bell peppers and mixed vegetables to the pan. Stir-fry for a few minutes until the vegetables are slightly tender.</li>
          <li>Add shredded chicken to the pan and mix well with the vegetables.</li>
          <li>In a small bowl, mix together soy sauce, oyster sauce, ketchup, and chili sauce. Pour the sauce over the chicken and vegetables in the pan.</li>
          <li>Add cooked rice to the pan and toss everything together until well combined.</li>
          <li>Season with salt and pepper to taste. Stir-fry for a few more minutes until the dish is heated through.</li>
          <li>Remove from heat and serve the Wington Hot Box hot.</li>
        </ol>
