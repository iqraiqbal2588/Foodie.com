      <h3>Samosa</h3>
      <img src="samosa.jpg" alt="Samosa" class="img-fluid">
      <p>
        Samosa is a popular Indian snack made with a crispy pastry shell filled with a savory mixture of potatoes,
        peas, and spices. These triangular delights are deep-fried until golden and served hot with chutney or
        ketchup. They are perfect as an appetizer or as a tea-time snack.
      </p>
      <p><strong>Ingredients:</strong></p>
      <ul>
        <li>All-purpose flour</li>
        <li>Potatoes, boiled and mashed</li>
        <li>Peas</li>
        <li>Onion, finely chopped</li>
        <li>Green chili, finely chopped</li>
        <li>Ginger, grated</li>
        <li>Spices (e.g., cumin seeds, coriander powder, garam masala)</li>
        <li>Oil, for frying</li>
      </ul>
      <p><strong>Instructions:</strong></p>
      <ol>
        <li>In a mixing bowl, combine the flour, salt, and oil. Gradually add water and knead to form a smooth dough.</li>
        <li>Divide the dough into small portions and roll each portion into a thin circle.</li>
        <li>In a separate pan, heat oil and add cumin seeds. Once they splutter, add the chopped onion, green chili,
          and grated ginger. Sauté until the onion turns golden.</li>
        <li>Add the mashed potatoes, peas, and spices. Mix well and cook for a few minutes.</li>
        <li>Remove from heat and let the mixture cool.</li>
        <li>Take a rolled dough circle and cut it in half. Fold one half into a cone shape, sealing the edges with
          water.</li>
        <li>Fill the cone with the potato mixture and seal the top edge with water.</li>
        <li>Repeat the process with the remaining dough and filling.</li>
        <li>Heat oil in a deep frying pan and deep-fry the samosas until golden brown and crispy.</li>
        <li>Remove from the oil and drain on a paper towel to remove excess oil.</li>
        <li>Serve hot with chutney or ketchup.</li>
      </ol>
