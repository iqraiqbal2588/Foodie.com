        <h3>Ingredients:</h3>
        <ul>
          <li>200g noodles</li>
          <li>2 tablespoons soy sauce</li>
          <li>1 tablespoon vinegar</li>
          <li>1 tablespoon chili sauce</li>
          <li>1 tablespoon sesame oil</li>
          <li>2 cloves garlic, minced</li>
          <li>1 small onion, sliced</li>
          <li>1 small carrot, julienned</li>
          <li>1 cup shredded cabbage</li>
          <li>1 cup bean sprouts</li>
          <li>2 green onions, chopped</li>
          <li>Salt and pepper to taste</li>
        </ul>

        <h3>Instructions:</h3>
        <ol>
          <li>Cook the noodles according to package instructions. Drain and set aside.</li>
          <li>In a small bowl, mix together soy sauce, vinegar, chili sauce, and sesame oil. Set aside.</li>
          <li>Heat oil in a pan or wok over medium heat. Add minced garlic and sliced onion, and saut√© until fragrant.</li>
          <li>Add julienned carrot, shredded cabbage, and bean sprouts to the pan. Stir-fry for a few minutes until vegetables are slightly tender.</li>
          <li>Add the cooked noodles to the pan and pour the sauce mixture over them. Toss everything together to coat the noodles and vegetables evenly.</li>
          <li>Cook for a few more minutes, stirring continuously, until the noodles are heated through.</li>
          <li>Season with salt and pepper to taste.</li>
          <li>Garnish with chopped green onions.</li>
          <li>Serve hot and enjoy!</li>
        </ol>
