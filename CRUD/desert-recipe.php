          </p>
          <p><strong>Ingredients:</strong></p>
          <ul>
            <li>Milk</li>
            <li>Sugar</li>
            <li>Cocoa powder</li>
            <li>Cornstarch</li>
            <li>Vanilla extract</li>
          </ul>
          <p><strong>Instructions:</strong></p>
          <ol>
            <li>In a saucepan, whisk together milk, sugar, cocoa powder, and cornstarch.</li>
