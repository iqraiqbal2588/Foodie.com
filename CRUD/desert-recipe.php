          <h3>Chocolate Cookies</h3>
          <img src="chocolate-cookies.jpg" alt="Chocolate Cookies" class="img-fluid">
          <p>
            Chocolate Cookies are a classic treat that everyone loves. These cookies are soft, chewy, and packed with
            chocolate flavor. Perfect for any occasion or as a delightful snack.
          </p>
          <p><strong>Ingredients:</strong></p>
          <ul>
            <li>All-purpose flour</li>
            <li>Cocoa powder</li>
            <li>Baking soda</li>
            <li>Salt</li>
            <li>Butter</li>
            <li>Sugar</li>
            <li>Eggs</li>
            <li>Vanilla extract</li>
            <li>Chocolate chips</li>
          </ul>
          <p><strong>Instructions:</strong></p>
          <ol>
            <li>In a bowl, whisk together flour, cocoa powder, baking soda, and salt.</li>
            <li>In a separate bowl, cream together butter and sugar until light and fluffy.</li>
            <li>Add eggs and vanilla extract to the butter mixture and mix well.</li>
            <li>Gradually add the dry ingredients to the wet ingredients and mix until combined.</li>
            <li>Stir in chocolate chips.</li>
            <li>Scoop the cookie dough onto a baking sheet and bake until the edges are set.</li>
            <li>Allow the cookies to cool before serving.</li>
            <li>Enjoy these delicious chocolate cookies!</li>
          </ol>
