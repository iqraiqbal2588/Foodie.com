        <h3>Nihari</h3>
        <p>A slow-cooked meat stew with rich and aromatic flavors.</p>
        <h4>Ingredients:</h4>
        <ul>
          <li>2 lbs beef or lamb, bone-in</li>
          <li>2 onions, thinly sliced</li>
          <li>4 tablespoons Nihari masala</li>
          <li>2 tablespoons ginger-garlic paste</li>
          <li>1/4 cup wheat flour</li>
          <li>1/4 cup ghee or oil</li>
          <li>4 cups water</li>
          <li>Salt to taste</li>
          <li>For garnish: ginger slices, chopped cilantro leaves, green chilies</li>
        </ul>
        <h4>Instructions:</h4>
        <ol>
          <li>In a large pot, heat ghee or oil and sauté the onions until golden brown.</li>
          <li>Add ginger-garlic paste and sauté for a minute. Then, add the meat and cook until it is browned.</li>
          <li>Add Nihari masala and salt. Mix well and cook for a few minutes.</li>
          <li>Add water to the pot and bring it to a boil. Reduce the heat to low and cover the pot. Simmer for 4-6 hours until the meat is tender and the flavors are well infused.</li>
          <li>In a small bowl, mix wheat flour with water to make a smooth paste. Add this paste to the pot and cook for another 30 minutes until the gravy thickens.</li>
          <li>Garnish with ginger slices, chopped cilantro leaves, and green chilies.</li>
          <li>Serve the Nihari hot with naan or rice.</li>
        </ol>
      </div>
