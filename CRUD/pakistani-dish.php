        <h3>Korma</h3>
        <p>A creamy and rich curry made with meat or vegetables.</p>
        <h4>Ingredients:</h4>
        <ul>
          <li>1 lb chicken or meat, cut into pieces</li>
          <li>2 onions, finely chopped</li>
          <li>4 tomatoes, pureed</li>
          <li>1/2 cup plain yogurt</li>
          <li>1 tablespoon ginger-garlic paste</li>
          <li>2 tablespoons Korma masala</li>
          <li>1/4 cup cashew nuts, soaked</li>
          <li>1/4 cup heavy cream</li>
          <li>2 tablespoons ghee or oil</li>
          <li>1/4 cup chopped cilantro leaves</li>
          <li>Salt to taste</li>
        </ul>
        <h4>Instructions:</h4>
        <ol>
          <li>In a pan, heat ghee or oil and sauté the onions until golden brown.</li>
          <li>Add ginger-garlic paste and sauté for a minute. Then, add the chicken or meat pieces and cook until they are browned.</li>
          <li>Add tomato puree, Korma masala, and salt. Cook until the tomatoes are cooked down and the oil separates.</li>
          <li>In a blender, blend soaked cashew nuts with water to make a smooth paste. Add this paste to the pan and mix well.</li>
          <li>Whisk the plain yogurt and add it to the pan. Cook for a few minutes until the yogurt is well incorporated.</li>
          <li>Add heavy cream and simmer for a few more minutes. Garnish with chopped cilantro leaves.</li>
          <li>Serve the Korma hot with rice or naan.</li>
        </ol>
      </div>
