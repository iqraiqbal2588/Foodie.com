        <h3>Pakory</h3>
        <p>Deep-fried fritters made with a batter of gram flour and various vegetables.</p>
        <h4>Ingredients:</h4>
        <ul>
          <li>1 cup gram flour (besan)</li>
          <li>1 onion, thinly sliced</li>
          <li>1 potato, thinly sliced</li>
          <li>1 green chili, finely chopped</li>
          <li>1/4 cup chopped cilantro leaves</li>
          <li>1/2 teaspoon red chili powder</li>
          <li>1/2 teaspoon cumin powder</li>
          <li>1/2 teaspoon turmeric powder</li>
          <li>1/2 teaspoon baking soda</li>
          <li>Salt to taste</li>
          <li>Water (as needed)</li>
          <li>Oil for deep frying</li>
        </ul>
        <h4>Instructions:</h4>
        <ol>
          <li>In a bowl, mix gram flour, red chili powder, cumin powder, turmeric powder, baking soda, and salt.</li>
          <li>Add sliced onions, potatoes, green chili, and cilantro leaves to the bowl. Mix well to coat the vegetables with the flour mixture.</li>
          <li>Gradually add water to the mixture and stir until you achieve a thick batter consistency.</li>
          <li>Heat oil in a deep pan or kadai for deep frying.</li>
          <li>Drop spoonfuls of the batter into the hot oil and fry until the pakoras turn golden brown and crispy.</li>
          <li>Remove the pakoras from the oil and drain on a paper towel to remove excess oil.</li>
          <li>Serve the pakoras hot with mint chutney or tamarind chutney.</li>
        </ol>
