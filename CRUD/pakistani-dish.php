      <h3>Alo matar gajjar:</h3>
        <h3>Ingredients:</h3>
        <ul>
          <li>2 potatoes, peeled and diced</li>
          <li>1 cup green peas (matar)</li>
          <li>2 carrots, peeled and diced</li>
          <li>1 onion, finely chopped</li>
          <li>2 tomatoes, chopped</li>
          <li>2 green chilies, slit</li>
          <li>1-inch piece of ginger, grated</li>
          <li>3-4 cloves of garlic, minced</li>
          <li>1/2 teaspoon cumin seeds</li>
          <li>1/2 teaspoon turmeric powder</li>
          <li>1/2 teaspoon red chili powder</li>
          <li>1/2 teaspoon garam masala</li>
          <li>2 tablespoons oil</li>
          <li>Salt to taste</li>
          <li>Fresh cilantro leaves for garnish</li>
        </ul>

        <h3>Instructions:</h3>
        <ol>
          <li>Heat oil in a pan over medium heat.</li>
          <li>Add cumin seeds and let them splutter.</li>
          <li>Add chopped onions and sauté until golden brown.</li>
          <li>Add grated ginger and minced garlic. Sauté for a minute.</li>
          <li>Add chopped tomatoes and cook until they turn mushy.</li>
          <li>Add turmeric powder, red chili powder, and salt. Mix well.</li>
          <li>Add diced potatoes, green peas, and carrots. Mix everything together.</li>
          <li>Cover the pan and cook on low heat until the vegetables are tender.</li>
          <li>Add garam masala and cook for another 2 minutes.</li>
          <li>Garnish with fresh cilantro leaves.</li>
          <li>Serve hot with roti, naan, or rice.</li>
        </ol>
