        <h3>Kabab</h3>
        <p>Grilled or fried minced meat patties seasoned with spices and herbs.</p>
        <h4>Ingredients:</h4>
        <ul>
          <li>1 lb ground meat (beef or chicken)</li>
          <li>1 onion, finely chopped</li>
          <li>2 cloves garlic, minced</li>
          <li>1/4 cup chopped cilantro leaves</li>
          <li>1/4 cup chopped mint leaves</li>
          <li>1 teaspoon ginger paste</li>
          <li>1 teaspoon garam masala</li>
          <li>1/2 teaspoon red chili powder</li>
          <li>1/2 teaspoon turmeric powder</li>
          <li>1/2 teaspoon cumin powder</li>
          <li>1/2 teaspoon coriander powder</li>
          <li>1/2 teaspoon black pepper powder</li>
          <li>1 egg, beaten</li>
          <li>Salt to taste</li>
        </ul>
        <h4>Instructions:</h4>
        <ol>
          <li>In a bowl, mix all the ingredients together until well combined.</li>
          <li>Cover and refrigerate the mixture for at least 30 minutes to marinate.</li>
          <li>Preheat the grill or heat oil in a pan for frying.</li>
          <li>Shape the meat mixture into small patties or kebabs.</li>
          <li>If grilling, cook the kebabs on medium heat until they are browned and cooked through.</li>
          <li>If frying, cook the kebabs in hot oil until they are golden brown on both sides.</li>
          <li>Serve the Kababs hot with mint chutney or yogurt sauce.</li>
        </ol>
