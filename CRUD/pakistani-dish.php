        <h3>Paye</h3>
        <p>A rich and hearty soup made with cow or lamb trotters.</p>
        <h4>Ingredients:</h4>
        <ul>
          <li>4-6 cow or lamb trotters</li>
          <li>2 onions, finely chopped</li>
          <li>4 tomatoes, chopped</li>
          <li>2 tablespoons ginger-garlic paste</li>
          <li>1 tablespoon red chili powder</li>
          <li>1 tablespoon turmeric powder</li>
          <li>1 tablespoon garam masala</li>
          <li>1/2 cup yogurt</li>
          <li>4 cups water</li>
          <li>4 tablespoons ghee or oil</li>
          <li>Salt to taste</li>
          <li>For garnish: ginger slices, chopped cilantro leaves, green chilies</li>
        </ul>
        <h4>Instructions:</h4>
        <ol>
          <li>In a pressure cooker, add ghee or oil and sauté the onions until golden brown.</li>
          <li>Add ginger-garlic paste and sauté for a minute. Then, add tomatoes, red chili powder, turmeric powder, garam masala, and salt. Cook until the tomatoes are soft.</li>
          <li>Add the trotters to the pressure cooker and cook for a few minutes to brown them.</li>
          <li>In a bowl, whisk yogurt with water and add it to the pressure cooker. Mix well.</li>
          <li>Add water and close the pressure cooker. Cook on high heat until the first whistle, then reduce the heat to low and cook for another 2 hours until the trotters are tender.</li>
          <li>Open the pressure cooker and simmer the Paye for another 30 minutes until the gravy thickens.</li>
          <li>Garnish with ginger slices, chopped cilantro leaves, and green chilies.</li>
          <li>Serve the Paye hot with naan or rice.</li>
        </ol>
