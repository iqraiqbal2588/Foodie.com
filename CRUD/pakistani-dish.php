        <h3>Biryani</h3>
        <p>Aromatic rice dish with flavorful spices and tender meat.</p>
        <h4>Ingredients:</h4>
        <ul>
          <li>2 cups Basmati rice</li>
          <li>1 lb chicken or meat, cut into pieces</li>
          <li>2 onions, thinly sliced</li>
          <li>4 tomatoes, chopped</li>
          <li>2 tablespoons Biryani masala</li>
          <li>1 tablespoon ginger-garlic paste</li>
          <li>1/2 cup plain yogurt</li>
          <li>1/4 cup chopped mint leaves</li>
          <li>1/4 cup chopped cilantro leaves</li>
          <li>4 cups water</li>
          <li>4 tablespoons ghee or oil</li>
          <li>Salt to taste</li>
        </ul>
        <h4>Instructions:</h4>
        <ol>
          <li>Wash the rice and soak it in water for 30 minutes.</li>
          <li>In a large pan, heat ghee or oil and sauté the onions until golden brown. Remove half of the onions for garnish.</li>
          <li>Add ginger-garlic paste to the pan and sauté for a minute. Then, add tomatoes, Biryani masala, and salt. Cook until the tomatoes are soft.</li>
          <li>Add the chicken or meat pieces to the pan and cook until they are browned.</li>
          <li>In a separate pot, bring water to a boil. Drain the soaked rice and add it to the boiling water. Cook the rice until it is 70-80% done.</li>
          <li>Drain the rice and layer it over the meat in the pan. Sprinkle chopped mint leaves and cilantro over the rice.</li>
          <li>Cover the pan with a tight-fitting lid and cook on low heat for 20-25 minutes until the rice is fully cooked and the flavors are well blended.</li>
          <li>Garnish with the reserved fried onions. Serve the Biryani hot.</li>
        </ol>
